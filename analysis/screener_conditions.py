# ============================================================================
# resolver 归类：技术面/阶段表现 多为 LOCAL_TECH；行情/基本/财务 多为 LOCAL_DATA；
# 个别(关注度/龙虎榜占比/机构评级/DDE/分时/涨停原因) 走 EXTERNAL 兜底。
# ============================================================================
_EXTERNAL_GROUPS = {'分时指标', 'DDE大单净量', 'DDE大单净额', '委比',
                    '龙虎榜机构买入占比', '机构评级', '关注度', '机构净额'}
_EXTERNAL_KEYS = {'涨停原因', '首次涨停时间', '涨停封单量', '涨停封单额',
                  '基金持股', '券商持股', '社保持股', '信托持股', '保险持股', 'QFII持股',
                  '大股东增持', '大股东减持', '高管增持', '高管减持',
                  '10转1以上', '10送1以上', '10派1以上'}


def _resolver_for(category: str, group: str, realname: str) -> str:
    if realname in _EXTERNAL_KEYS or group in _EXTERNAL_GROUPS:
        return EXTERNAL
    if category in ('技术面', '阶段表现'):
        return LOCAL_TECH
    if category in ('行情面', '基本面', '财务面'):
        return LOCAL_DATA
    return EXTERNAL


# 构建扁平注册表
REGISTRY: List[Condition] = []
for _cat, _groups in _TREE.items():
    for _grp, _leaves in _groups.items():
        for _lf in _leaves:
            REGISTRY.append(Condition(
                category=_cat, group=_grp, name=_lf, realname=_lf,
                resolver=_resolver_for(_cat, _grp, _lf),
            ))

_BY_NAME = {c.realname: c for c in REGISTRY}


def find(query: str) -> Optional[Condition]:
    """按 realname 精确 / 模糊查条件。"""
    if query in _BY_NAME:
        return _BY_NAME[query]
    for c in REGISTRY:
        if query in c.realname or query in c.name:
            return c
    return None


def list_supported(resolver: str = None) -> List[Condition]:
    """列出某 resolver 的条件(默认全部)。"""
    return [c for c in REGISTRY if resolver is None or c.resolver == resolver]


def stats() -> Dict[str, int]:
    from collections import Counter
    c = Counter(x.resolver for x in REGISTRY)
    return {'total': len(REGISTRY), **dict(c)}


