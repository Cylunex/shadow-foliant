DEFAULT_LENSES = ['buffett', 'duan', 'jiucai']  # A股价值+反向,默认组合


def build_review_prompt(stock_brief: str, decision_brief: str,
                        lenses: List[str] = None) -> str:
    """生成投资哲学透镜复核 prompt。

    Args:
        stock_brief:   个股关键信息摘要(代码/名称/价/估值/基本面要点)。
        decision_brief:多智能体得出的结论摘要(方向/理由/风险)。
        lenses:        启用的透镜(默认 buffett+duan+jiucai)。
    """
    lenses = lenses or DEFAULT_LENSES
    blocks = []
    for k in lenses:
        lz = LENSES.get(k)
        if lz:
            blocks.append(f"【{lz['name']}】\n{lz['checklist']}")
    lens_text = "\n\n".join(blocks)
    return f"""你是一位资深投资哲学顾问。请**不要重复**前面分析师的技术/财务细节,
而是用下列投资大师的思维框架,对这只股票的**投资决策本身**做一次高层"哲学复核",
指出共识里可能被忽略的风险、估值与人性偏差,给出独立的"赞成/保留/反对"意见。

个股信息:
{stock_brief}

多智能体初步结论:
{decision_brief}

请依次用每个透镜审视,并在最后给出综合判断:

{lens_text}

输出要求:
1. 每个透镜给 2-4 句关键审视(命中的风险/亮点),不空泛。
2. 最后\"哲学复核结论\":在多智能体结论之上,你是【加强买入信心 / 维持 / 降低信心/转谨慎 / 反对】中的哪一档,一句话理由。
3. 若触发韭菜反向信号,明确点出\"当前更像韭菜思维的地方\"。
"""


